package Loops;

public class WhileLoop2_printNumFrom_11_to_50
{
	public static void main(String[] args)
	{
		
		int i=11;     //start num
		
		//   11<=50
		while(i<=50)       //end num
		{
			System.out.println(i);         //11
			
			i++;        //incr or decr
		}
		
		
	}

}
