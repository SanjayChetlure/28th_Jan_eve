package Loops;

public class WhileLoop5_printNumFrom_5_to_1
{
	public static void main(String[] args)
	{
		//start num:  5
		//end num:    1
		//incr/decr :  decr by 1
		
		
		int i=5;     //start num
		
		//    0>=1
		while(i>=1)       //end num
		{
			System.out.println(i);        //5 4 3 2 1
			
			i--;    //0   //incr or decr
		}
		
		
	}

}
