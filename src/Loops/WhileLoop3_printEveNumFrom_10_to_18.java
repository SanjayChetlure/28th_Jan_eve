package Loops;

public class WhileLoop3_printEveNumFrom_10_to_18
{
	public static void main(String[] args)
	{
		
		int i=10;        //start num
		
		//  20<=18
		while(i<=18)       //end num
		{
			System.out.println(i);         // 10 12 14 16 18
			
			i=i+2;     // 20  //incr or decr
		}
		
		
	}

}
