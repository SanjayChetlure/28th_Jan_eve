package Loops;

public class ForLoop5_printNumFrom_5_to_1
{
	public static void main(String[] args)
	{
		//print num from 5 to 1
		
		//start num: 5
		//end num:  1
		//incr or decr:  decr by 1
				
		//        5  0>=1    0
		for(int i=5; i>=1; i--)
		{                    //1
			System.out.println(i);
		}  
		
		//5
		//4
		//3
		//2
		//1
		
	
	}

}
