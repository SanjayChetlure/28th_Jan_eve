package Loops;

public class DoWhile1_printNumFrom1_to_5
{
	public static void main(String[] args)
	{
		
		int i=1;     //start num
		
		do
		{
			System.out.println(i);      //1 2 3 4 5   //print stat
			
			i++;             //6      //incr or decr
		}
		while(i<=5);         //end num
		//    6<=5
		
		
	}

}
